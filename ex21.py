def addition(a,b):
    print(f"addition of {a} and {b}")
    return a+b

def subtraction(a,b):
    print(f"subtracting {a} and {b}")
    return a-b

def multiplication(a,b):
    print(f"Multiplication of {a} and {b}")
    return a*b

def division(a,b):
    print(f"dividing {a} and {b}")
    return a/b


print(addition(3,5))
print(subtraction(100,20))
print(multiplication(2,3))
print(division(20,3))